    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        Cliente cliente = new Cliente();
        try {
            cliente.cliente(TelaInicial.ipAux, TelaInicial.portaAux);
            String resultado = cliente.cadastrarSe(nome.getText(), cpf.getText(), senha.getText(), confirmaSenha.getText());
            JOptionPane.showMessageDialog(null, resultado);
            if(resultado.equals("Cadastro efetuado com sucesso")){
                Login.telaLogin = new Login();
                this.setVisible(false);
                Login.telaLogin.setVisible(true);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Erro de IO");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro de Class");
        }
        cliente.sair();
    }//GEN-LAST:event_cadastrarActionPerformed

